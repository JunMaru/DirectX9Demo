//*****************************************************************************
LPDIRECTINPUT8 CInput::m_pDInput = NULL;

//=============================================================================
// コンストラクタ
//=============================================================================
CInput::CInput()
{

}

//=============================================================================
// デストラクタ
//=============================================================================
CInput::~CInput()
{

}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CInput::Init(HINSTANCE hInstance, HWND hWnd)
{
  UNREFERENCED_PARAMETER(hWnd);
	HRESULT hr;

	// DirectInputオブジェクトの作成
	if (m_pDInput == NULL)
	{
		hr = DirectInput8Create(hInstance,
			DIRECTINPUT_VERSION,
			IID_IDirectInput8,
			(void**)&m_pDInput,
			NULL);

		if (FAILED(hr))
		{
			MessageBox(NULL, "オブジェクトの作成に失敗", "faild", MB_YESNO | MB_ICONQUESTION);
			return hr;
		}
	}

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CInput::Uninit(void)
{
	// DirectInputオブジェクトの開放
	if (m_pDInput != NULL)
	{
		m_pDInput->Release();
		m_pDInput = NULL;
	}
}